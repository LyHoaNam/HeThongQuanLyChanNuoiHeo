    private boolean KiemTraCacTruongBatBuoc_PhieuNhap()
    {
        //Lấy số lượng dong của jTable
        int countRow=tblChiTietPhieuNhap_Them.getRowCount();
        //Kiểm tra từng dòng xem số lượng và thành tiền có null hay bằng 0 hay không. Thành tiền có thể bằng 0. Cả 2 đều không được âm
        for(int i=0;i<countRow;i++)
        {
            //lấy giá trị "số lượng", "thành tiền" trong bảng ra tại dòng thứ i
            String strsoluong=tblChiTietPhieuNhap_Them.getValueAt(i, 2).toString();
            String strthanhtien=tblChiTietPhieuNhap_Them.getValueAt(i, 4).toString();
            //Chuyển giá trị số lượng và thành tiền từ String thành double
            double soluong=Double.parseDouble(strsoluong);
            double thanhtien=Double.parseDouble(strthanhtien);
            //kiểm tra nếu số lượng <= 0 hoặc thành tiền <0 thì báo lỗi
            if(soluong<=0 || thanhtien<0)
            { 
                JOptionPane.showMessageDialog(null, "Giá trị số lượng hoặc thành tiền không hợp lệ.");
                return false;
            }
            
        }
        //Kiểm tra ngày nhập kho có hợp lệ hay không
        DateFormat df= new SimpleDateFormat("yyyy-MM-dd");
        
        try{
              String ngayNhapKho=df.format(dcNgayNhapKho_Them.getDate());
        }
        catch(Exception ex)
        {
             JOptionPane.showMessageDialog(null, "Ngày nhập không hợp lệ.");
             return false;
        }
        
        return true;
    }
